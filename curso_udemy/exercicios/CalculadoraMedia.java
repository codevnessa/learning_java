package exercicios;
public class CalculadoraMedia {
    public static void main(String[] args) {
        double numero1 = 8.5;
        double numero2 = 7.0;
        double numero3 = 9.5;
        double media = (numero1 + numero2 + numero3) / 3;
        System.out.println("A média é: " + media);
    }
}