
public class MetodosMath {
    public static void main(String[] args) {
        // Exemplos de uso dos métodos da classe Math
        System.out.println("Valor absoluto de -5: " + Math.abs(-5));
        System.out.println("Raiz quadrada de 16: " + Math.sqrt(16));
        System.out.println("2 elevado à potência de 3: " + Math.pow(2, 3));
        System.out.println("Arredondamento para cima de 4.3: " + Math.ceil(4.3));
        System.out.println("Arredondamento para baixo de 4.7: " + Math.floor(4.7));
        System.out.println("Arredondamento de 4.5: " + Math.round(4.5));
        System.out.println("Maior valor entre 10 e 20: " + Math.max(10, 20));
        System.out.println("Menor valor entre 10 e 20: " + Math.min(10, 20));
        System.out.println("Seno de 90 graus: " + Math.sin(Math.toRadians(90)));
        System.out.println("Cosseno de 0 graus: " + Math.cos(Math.toRadians(0)));
        System.out.println("Tangente de 45 graus: " + Math.tan(Math.toRadians(45)));
        System.out.println("Arco seno de 1: " + Math.toDegrees(Math.asin(1)));
        System.out.println("Arco cosseno de 0: " + Math.toDegrees(Math.acos(0)));
        System.out.println("Arco tangente de 1: " + Math.toDegrees(Math.atan(1)));
        System.out.println("Conversão de 180 graus para radianos: " + Math.toRadians(180));
        System.out.println("Conversão de π radianos para graus: " + Math.toDegrees(Math.PI));
        System.out.println("e elevado à potência de 1: " + Math.exp(1));
        System.out.println("Logaritmo natural de e: " + Math.log(Math.E));
        System.out.println("Logaritmo na base 10 de 100: " + Math.log10(100));
        System.out.println("Número aleatório entre 0 e 1: " + Math.random());
        System.out.println("Valor de π: " + Math.PI);
        System.out.println("Valor de e: " + Math.E);
    }
}
